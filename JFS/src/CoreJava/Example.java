package CoreJava;

public class Example {

	public static void main(String[] args) {
		int a=7,b=6,i;
		for(i=0;i<=1;i++)
		{
		System.out.println("a+b="+(a+b));
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a%b);
		}
		i=1;
		while(i<=10)
		{
		System.out.println(2*i);
		i=i+1;
		}
		int j=1;
		do
		{
		System.out.println(3*j);
		j=j+1;
		}while(j<=10);
		

	}

}
